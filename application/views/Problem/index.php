    <h1 id="首页"><a href="#首页" class="headerlink" title="首页"></a>首页</h1>

    <h2 id="OJ"><a href="#OJ" class="headerlink" title="OJ"></a>苏州大学 Online Judge</h2>

    <p> 此在线评测系统用于计算机科学与技术学院编程作业、练习、考试，以及计算机算法程序设计竞赛训练使用。</p>
    <p> 此系统由<a href="http://115.28.164.8">苏州大学微软学生俱乐部</a>设计开发，于2017年4月正式开始，预计到6月结束。</p>                   


    <h2 id="mainfunction"><a href="#mainfunction" class="headerlink" title="主要功能"></a> 主要功能：</h2>
    <ul>
        <li> 自主命题，添加题目和数据</li>
        <li> 自主开设比赛，题目可以新建也可以从题库中选取</li>
        <li> 系统保留成功通过的代码可以回看</li>
        <li> 导出个人成功通过的代码</li>
    </ul>

    <h2 id="适用范围"><a href="#适用范围" class="headerlink" title="适用范围"></a>适用范围</h2>
    <ul>
        <li> ACM-ICPC形式的比赛、训练</li>
        <li> C/C++、python等的客观题作业评测</li>
        <li> 有关公司或实验室上机笔试、训练</li>
    </ul>

    <h2 id="技术组成"><a href="#技术组成" class="headerlink" title="技术组成"></a>技术组成</h2>
    <ul>
        <li> 前端使用bootstrap框架、JQuery，并往AngularJS迁移。</li>
        <li> 后台简单数据处理使用PHP7.0</li>
        <li> 数据库使用mysql</li>
        <li> 测评内核目前使用php，但是存在一些问题，正在迁移到C++做测评内核。</li>
        <li> 服务器目前在阿里云 Ubuntu14.04</li>
    </ul>

    <h2 id="前端组成"><a href="#前端组成" class="headerlink" title="前端组成："></a>前端组成：</h2>
    <ul>
        <li> 首页</li>
        <li> 题目列表</li>
        <li> 比赛列表</li>
        <li> 题目页</li>
        <li> 提交记录</li>
        <li> 比赛实时</li>
        <li> 个人页</li>
        <li> 添加比赛</li>
        <li> 添加题目</li>
        <li> 注册、登录</li>
    </ul>

    <h3> 未来规划：</h3>
